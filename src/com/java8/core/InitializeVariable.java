package com.java8.core;

public class InitializeVariable {

	public static void main(String[] args) {
		int number;
		number = 15;
       System.out.println("number" + number);
       
       // one line
       double myDoube = 10.2;
       float fiver = 4.0f , sixer = 7.0f;
       boolean b1; int i; String s1, s2;
     //  int i3; i4    will both compile because i4 is treated as separate.
       
       // Identifiers can start with $ sign or underscore.
       
       int $four;
       double  _start;
       
       
       
	}

}
